# Menggunakan List 
angka_list = [0,1,2,3,4] #Ini Adalah List

print (" Menggunakan List ".center(30, "="))
for i in angka_list :
    print (f"i sekarang -> {i}")

print (" ")


# Menggunakan Range 
angka_range = range(5)

print (" Menggunakan Range ".center(30, "="))
for i in angka_range :
    print (f"i sekarang -> {i}")

print (" ")

angka_range = range(5,10)

print (" Menggunakan Range ".center(30, "="))
for i in angka_range :
    print (f"i sekarang -> {i}")

print (" ")


# Menggunakan String 
data_str = "Hello Hesti Cantik"

